// 6. Find the Character Is Vowel or Not
#include<stdio.h>
int main(){

    char ch;

    printf("Enter a character to find its vowel or not: ");
    scanf("%c",&ch);

    if(ch== 'a' ){

        printf("\n%c is a vowel.",ch);
    }
    else if(ch== 'e' ){

        printf("\n%c is a vowel.",ch);
    }
    else if(ch== 'u' ){

        printf("\n%c is a vowel.",ch);
    }
    else if(ch== 'i' ){

        printf("\n%c is a vowel.",ch);
    }
    else if(ch== 'o' ){

        printf("\n%c is a vowel.",ch);
    }
    else
    {
        printf("\n%c is not a vowel.",ch);
    }

}